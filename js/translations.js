function getTranslation(key, lang = 'ru') {
    if (window.translations[key] && window.translations[key][lang]) {
        return window.translations[key][lang];
    }
    // Если перевод не найден, возвращаем русский вариант
    return window.translations[key] ? window.translations[key]['ru'] : key;
}

